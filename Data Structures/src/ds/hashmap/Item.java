package ds.hashmap;

public class Item {

	Integer key;
	Integer value;

	public Item() {
		key = null;
		value = null;
	}

	public Integer getKey() {
		return key;
	}

	public void setKey(Integer key) {
		this.key = key;
	}

	public Integer getValue() {
		return value;
	}

	public void setValue(Integer value) {
		this.value = value;
	}

}
